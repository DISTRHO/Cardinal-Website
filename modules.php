							<h3>Adding modules</h3>
							<p>Installing new modules on a Cardinal build is not possible, but we can integrate existing open-source modules to be part of Cardinal.<br/>
							Details on this are available <a href="https://github.com/DISTRHO/Cardinal/discussions/28" target="_blank">here</a>.<br/>
							Also check <a href="https://github.com/DISTRHO/Cardinal/wiki/Possible-modules-to-include" target="_blank">this wiki page</a>
							where we discuss possible modules to include.</p>
						</div>
						<div class="row-6 row-12-mobilep">
							<h3>Module restrictions</h3>
							<p>All included modules are open-source and have a GPLv3+ compatible license. (GPLv3-only modules are not allowed)<br/>
							It is a requirement that the final Cardinal binary is GPLv3+ licensed.</p>
							<p>Module dependencies should be kept at a minimum, as otherwise it quickly grows the complexity of the build.<br/>
							Online access (such as phone-home) is not allowed.</p>
							<p>Worth noting that a few modules have artwork licensed separately from their code.<br/>
							These licenses range from CC-0 to CC-NC-ND to custom (used with permission).<br/>
							An overview of the included artwork licenses can be seen <a href="https://github.com/DISTRHO/Cardinal/blob/main/docs/LICENSES.md#artwork--panel-licenses" target="_blank">here</a>.</p>
							<p>Even though CC-NC is problematic for packaging (some linux distributions are commercial in nature),
							Cardinal allows their use because of how prevalent they are across many Rack modules.<br/>
							Even the Rack "Component Library" (which can be considered the base widget elements) is CC-NC licensed.</p>
							<p>Any artwork that uses a custom license has had explicit permission to be used in Cardinal.</p>
